# . Вам дан кортеж. Вывести его в обратном порядке.
# ('n', 'o', 'h', 't', 'y', 'p', ' ', ',', 'u', 'o', 'y', ' ', 'e', 'v', 'o', 'l', ' ', 'i')


a = ('n', 'o', 'h', 't', 'y', 'p', ' ', ',', 'u', 'o', 'y', ' ', 'e', 'v', 'o', 'l', ' ', 'i')

b = [*a]
b.reverse()
s = ''
for i in b:
    s+=i
print(s)
